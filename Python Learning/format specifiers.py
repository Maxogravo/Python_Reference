price1 = 3124.14159

print(f"price 1 is £{price1: .2f}") #.2f shows how many decimal places we want
print(f"price 1 is £{price1: 10}") #shows how many spaces we want to take up
print(f"price 1 is £{price1: >24}") #shows what position we want to justify(<,>)
print(f"price 1 is £{price1: ,}") #seperates thousands